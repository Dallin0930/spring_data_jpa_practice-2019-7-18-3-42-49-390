CREATE TABLE cases (
caseId int(50),
caseName varchar(255),
caseTime  varchar(100)
);

create Table caseDetail (
caseDetailId int(50),
subjectiveDetail varchar(255),
objectiveDetail varchar(255)
);


CREATE TABLE Justice (
    justiceId int,
    name varchar(100),
    age int(255),
    sex varchar(10),
    birthday varchar(50),
    id_number int(100),
    experience int(50)
);

CREATE TABLE Prosecutor (
    proId int(50),
    proName Varchar(255),
 );




